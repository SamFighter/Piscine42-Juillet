void	ft_swap(int *a, int *b)
{
	int	c;

	c = *a;
	*a = *b;
	*b = c;
}

void	ft_rev_int_tab(int *tab, int size)
{
	int	ch1;

	ch1 = 0;
	while (ch1 < size / 2)
	{
		ft_swap(&tab[ch1], &tab[size - (ch1 + 1)]);
		ch1++;
	}
}
